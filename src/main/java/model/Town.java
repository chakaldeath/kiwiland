package model;

public class Town {

	private Long idTown;
	private String name;
	
	
	public Long getIdTown() {
		return idTown;
	}
	public void setIdTown(Long idTown) {
		this.idTown = idTown;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
